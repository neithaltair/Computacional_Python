#Cadenas y entradas con python

my_str = 'Neith Altair'

#Imprime el contenido de la variable
print(my_str)
print()
#Imprime posiciones especificas
print(my_str[0])
print(my_str[2])
print(my_str[5])
print()
#Imprime desde N hasta el final
print(my_str[2:])
print()
#Imprime desde el inicio hasta N
print(my_str[:5])
print()
#Imprime desde N hasta el inicio
print(my_str[:-3])
print()
#Imprime desde el inicio hasta el fin de 2 en 2
print(my_str[::2])

